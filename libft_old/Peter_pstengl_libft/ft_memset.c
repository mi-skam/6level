#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	counter;

	counter = 0;
	while (counter < n)
	{
		((unsigned char *)s)[counter] = (unsigned char)c;
		counter++;
	}
	return (s);
}
